// Implicit Conversion to Number
// numeric string used with - , / , * results number type

let result;

result = '4'-'2';//If you use string numbers and string number with number with arithemetic '-' operator  
console.log(result);

result = '4'-2;
console.log(result);

result = '4'*2;//String number with number with arithmetic operator '*'
console.log(result);

result = '4'/2;//String number with number with arithmetic operator '/'
console.log(result);